package com.project.dto;

public class HeartDTO {


    private int hno;
    private int hcheck;
    private int bno;
    private int idx;


    public int getHno() {
        return hno;
    }

    public void setHno(int hno) {
        this.hno = hno;
    }

    public int getHcheck() {
        return hcheck;
    }

    public void setHcheck(int hcheck) {
        this.hcheck = hcheck;
    }

    public int getBno() {
        return bno;
    }

    public void setBno(int bno) {
        this.bno = bno;
    }

    public int getIdx() {
        return idx;
    }

    public void setIdx(int idx) {
        this.idx = idx;
    }


}
